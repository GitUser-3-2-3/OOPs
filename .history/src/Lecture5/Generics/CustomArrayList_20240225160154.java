package Lecture5.Generics;

import java.util.ArrayList;

/**
 * CustomArrayList
 */
public class CustomArrayList {
    public static void main(String[] args) {
        System.out.println();

        ArrayList<Integer> list = new ArrayList<>();
        list.add(21);
        System.out.println(list.get(0));

        System.out.println();
    }
}